DEFAULT_POLICY_URL = "https://platform.cintara.io/policy"
DEFAULT_REGISTRY_URL = "https://platform.cintara.io/registry"
DEFAULT_GATEWAY_URL = "https://gateway.cintara.io"
DEFAULT_TOOL_NAME = "send_email"


@dataclass(frozen=True)
class InitConfig:
    agent_id: str
    tenant_id: str
    policy_url: str
    registry_url: str
    gateway_url: str
    api_token: str
    tool_name: str = DEFAULT_TOOL_NAME


def _clean_url(value: str) -> str:
    return value.strip().rstrip("/")


def _quote(value: str) -> str:
    return shlex.quote(value)


def _is_placeholder(value: str) -> bool:
    return not value or value.startswith("<") or value.endswith(">")


def _prompt(label: str, current: str | None = None, *, secret: bool = False) -> str:
    if current:
        return current
    if not sys.stdin.isatty():
        return ""
    prompt = f"{label}: "
    if secret:
        return getpass.getpass(prompt).strip()
    return input(prompt).strip()


def build_env_file(config: InitConfig) -> str:
    return "\n".join(
        [
            "# Cintara LangGraph configuration",
            "# Load with: source .env.cintara",
            f"export CINTARA_POLICY_URL={_quote(config.policy_url)}",
            f"export CINTARA_REGISTRY_URL={_quote(config.registry_url)}",
            f"export CINTARA_GATEWAY_URL={_quote(config.gateway_url)}",
            f"export CINTARA_API_TOKEN={_quote(config.api_token)}",
            f"export CINTARA_TENANT_ID={_quote(config.tenant_id)}",
            f"export CINTARA_AGENT_ID={_quote(config.agent_id)}",
            f"export CINTARA_DEMO_TOOL_NAME={_quote(config.tool_name)}",
            "",
        ]
    )


def build_guard_file() -> str:
    return textwrap.dedent(
        '''
        """Small Cintara helper for LangGraph workflows.

        Generated by `cintara-langgraph init`.
        Keep Cintara tokens server-side. Do not import this file in browser code.
        """

        from __future__ import annotations

        import os

        from cintara_langgraph import CintaraGuard

        try:
            from langgraph.graph import END
        except ImportError:  # Allows importing this file before LangGraph is installed.
            END = "__end__"


        def build_cintara_guard(*, interrupt_on_approval: bool = False) -> CintaraGuard:
            return CintaraGuard(
                agent_id=os.environ["CINTARA_AGENT_ID"],
                interrupt_on_approval=interrupt_on_approval,
            )


        def add_cintara_guard(
            builder,
            *,
            tools_node: str = "tools",
            approval_node: str | None = None,
            end_node=END,
            interrupt_on_approval: bool = False,
        ) -> CintaraGuard:
            """Add Cintara as the pre-tool guard node.

            Routes:
            - allow: continue to the tool execution node
            - approval: route to approval_node if provided, otherwise end
            - deny/error: end safely
            """

            guard = build_cintara_guard(interrupt_on_approval=interrupt_on_approval)
            builder.add_node("cintara", guard)
            builder.add_conditional_edges(
                "cintara",
                guard.route,
                {
                    "allow": tools_node,
                    "approval": approval_node or end_node,
                    "deny": end_node,
                    "error": end_node,
                },
            )
            return guard
        '''
    ).lstrip()


